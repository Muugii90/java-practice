package day17_stringpool_whileloop;

public class WhileLoop2 {

	public static void main(String[] args) {
		int x =0;
		while (x<5) {
			System.out.println("Happy New Year");
			++x;
		}
		int num = 0;
		while (num <= 4) {
			System.out.println("Java is Fun!!!");
			num++;
		}
		System.out.println("End here");
	}

}
