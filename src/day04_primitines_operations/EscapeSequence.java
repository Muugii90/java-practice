package day04_primitines_operations;

public class EscapeSequence { // escape character \", \', 

	public static void main (String [] args ) {
		 //print He said "Hello".
		System.out.println ("He said \"Hello\".");
		
		System.out.println (" Would you like \'coffee\' or \'tea\'?");
		 
		System.out.println (" \"\\__/\" "); //Backslash
		
		System.out.println (" Java\\n Selenium");
		
		System.out.println (" Java\tC## \n CSS");
	
		
	}
}

